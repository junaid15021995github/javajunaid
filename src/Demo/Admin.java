package Demo;

public class Admin {
String name;
String mobile;
String password;
public Admin(String name,String mobil,String password) {
	this.name=name;
	this.mobile=mobile;
	this.password=password;
}
public String getName() {
	return name;
}
public String getMobile() {
	return mobile;
}
public String getPassword() {
	return password;
}

}
